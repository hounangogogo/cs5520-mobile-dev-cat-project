class Animal {
    constructor(id, name, breeds, color, age, size, gender, species, phone, email, address, description, imageUri) {
        this.id = id;
        this.name = name;
        this.breeds = breeds;
        this.color = color;
        this.age = age;
        this.size = size;
        this.gender = gender;
        this.species = species;
        this.phone = phone;
        this.email = email;
        this.address = address;
        this.description = description;
        this.imageUri = imageUri;
    }
}


export default Animal;

